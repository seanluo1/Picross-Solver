# checks if all columns still have configurations after placing a row
# Returns True if column still has possible configurations, if not returns False
def validPlacement(solution, colValues, col_index, alreadyPlaced):
    if (col_index >= len(colValues)): return True
    failure = False
    for config in colValues[col_index]:
        failure = False
        for index in alreadyPlaced:
            if config[index] != solution[index][col_index]:
                failure = True
                break
        if not failure:
            break

    if not failure: # check next column
        return validPlacement(solution, colValues, col_index + 1, alreadyPlaced)
    else: return False # reaches here if any column is completely out of configurations

# place a possible row configuration into the nonogram
def placeRow(nonogram, row, index):
    nonogram[index] = row

# combinationHelper
# INPUTS: line -- 2D list where each element is a block of colors
#              -- example: [1, 1]
#         size -- height of row/col
#
# RETURN VALUE: values -- list of all possible iterations of the variable in string format
#                      -- example: ['1010', '1001', '0101']
#
# code inspired from Shihab Shahriar
# via https://stackoverflow.com/questions/47159012/recursive-algorithm-to-find-all-possible-solutions-in-a-nonogram-row
def combinationHelper(line, size):
    if not line: return ["0"*size]
    if line[0] > size: return []

    starts = size - line[0] # starts are the possible starting indexes of the blocks of color
    if len(line) == 1: # special case where there is only one block of color in the row
        return [("0"*i+"1"*line[0]+"0"*(starts-i)) for i in range(starts+1)]

    ans = []
    for i in range(size - line[0]): # append current solutions
        for sol in combinationHelper(line[1:],starts-i-1): # with all possible other solutions
            ans.append("0"*i+"1"*line[0]+"0"+sol)

    return ans

# produceCombinations
# INPUTS: line -- 2D list where each element is a line of colors
#              -- example: [1, 1]
#         size -- height of row/col
#
# RETURN VALUE: values -- list of all possible iterations of the variable in integer format
#                      -- example: [[1010], [1001], [0101]]
#
def produceCombinations(line, size):
    allValues = []
    solution = combinationHelper(line, size)
    for elem in solution:
        combination = []
        for num in elem:
            combination.append(ord(num) - 48)
        allValues.append(combination)
    return allValues
